import { readFile, writeFile } from "node:fs/promises";
import { createRequire } from "node:module";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const ts = require("typescript");

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const sourceDirectory = join(scriptDirectory, "..", "src");
const generatedPath = join(sourceDirectory, "bundle-source.generated.ts");
const moduleNames = ["audioHelpers", "behavior", "bounds", "camera", "combat", "feedback", "flight", "gameplay", "numeric", "rigs", "rotation", "types", "vectors"];
const publicRuntimeExports = [
  "AngleEx", "ArrayEx", "AudioCueEx", "BasisEx", "BoresightEx", "Bounds2", "Bounds3", "CameraMath", "CheckpointRaceEx", "ColorEx", "ControllerEx", "CoordinatedTurnEx", "Ease", "FlightRig", "FxEx", "GuidedFlightEx", "GunneryEx", "HitTestEx", "InputEx", "MaterialEx", "MotionEx", "Mathf", "NumberEx", "ProjectileEx", "PropellerEx", "Quat", "RandomEx", "RespawnEx", "ShipFxEx", "SpawnEx", "TextEx", "TimerEx", "TransformMath", "Vector2", "Vector3", "TriggerEx", "Vec2", "Vec3", "CameraRig", "CharacterRig", "KinematicMoverEx", "defineBehavior",
];

const sources = new Map();
for (const moduleName of moduleNames) {
  const path = join(sourceDirectory, `${moduleName}.ts`);
  const source = (await readFile(path, "utf8")).replace(/^\uFEFF/, "").replace(/\r\n?/g, "\n");
  const sourceFile = ts.createSourceFile(path, source, ts.ScriptTarget.ES2023, true, ts.ScriptKind.TS);
  sources.set(moduleName, { path, source, sourceFile });
}

const moduleData = new Map();
for (const [moduleName, module] of sources) {
  moduleData.set(moduleName, analyzeModule(moduleName, module.sourceFile));
}

const order = topologicalOrder(moduleData);
const moduleIndexes = new Map(order.map((moduleName, index) => [moduleName, index]));
const blocks = [];
for (const moduleName of order) {
  const data = moduleData.get(moduleName);
  const moduleIndex = moduleIndexes.get(moduleName);
  if (data === undefined || moduleIndex === undefined) {
    continue;
  }
  blocks.push(generateModule(moduleName, data, moduleIndexes));
}

for (const exportName of publicRuntimeExports) {
  const owner = order.find((moduleName) => moduleData.get(moduleName)?.exports.has(exportName));
  if (owner === undefined) {
    throw new Error(`Canonical stdlib export '${exportName}' is not present in the runtime source graph.`);
  }
  const index = moduleIndexes.get(owner);
  blocks.push(`const ${exportName} = __tn_stdlib_module_${index}[${JSON.stringify(exportName)}];`);
}

const bundle = [
  "// Generated from packages/script-stdlib/src canonical modules. Do not edit.",
  ...blocks,
].join("\n");
const generated = `// Generated by scripts/generate-bundle-source.mjs. Do not edit.\nexport const SCRIPT_STDLIB_BUNDLE_SOURCE = ${JSON.stringify(bundle)};\nexport const SCRIPT_STDLIB_RUNTIME_EXPORTS = ${JSON.stringify(publicRuntimeExports)} as const;\n`;

if (process.argv.includes("--check")) {
  let current;
  try {
    current = await readFile(generatedPath, "utf8");
  } catch {
    current = undefined;
  }
  if (current !== generated) {
    process.stderr.write(`Generated stdlib bundle is stale: ${generatedPath}\n`);
    process.exitCode = 1;
  } else {
    process.stdout.write("Generated stdlib bundle is current.\n");
  }
} else {
  await writeFile(generatedPath, generated, "utf8");
  process.stdout.write(`Generated ${generatedPath}\n`);
}

function analyzeModule(moduleName, sourceFile) {
  const dependencies = new Set();
  const imports = [];
  const body = [];
  const exports = new Map();
  const starDependencies = [];

  for (const statement of sourceFile.statements) {
    if (ts.isImportDeclaration(statement)) {
      const specifier = literalSpecifier(statement.moduleSpecifier);
      const dependency = resolveLocalModule(specifier);
      const clause = statement.importClause;
      if (dependency === undefined || clause === undefined || clause.isTypeOnly) {
        continue;
      }
      dependencies.add(dependency);
      imports.push({ clause, dependency });
      continue;
    }
    if (ts.isExportDeclaration(statement)) {
      const specifier = statement.moduleSpecifier === undefined ? undefined : literalSpecifier(statement.moduleSpecifier);
      const dependency = resolveLocalModule(specifier);
      if (dependency !== undefined) {
        dependencies.add(dependency);
        if (statement.exportClause !== undefined && ts.isNamedExports(statement.exportClause)) {
          for (const element of statement.exportClause.elements) {
            if (element.isTypeOnly) {
              continue;
            }
            exports.set(element.name.text, { dependency, localName: element.propertyName?.text ?? element.name.text });
          }
        } else {
          starDependencies.push(dependency);
        }
      } else if (statement.moduleSpecifier === undefined && statement.exportClause !== undefined && ts.isNamedExports(statement.exportClause)) {
        for (const element of statement.exportClause.elements) {
          if (!element.isTypeOnly) {
            exports.set(element.name.text, { localName: element.propertyName?.text ?? element.name.text });
          }
        }
      }
      continue;
    }
    if (ts.isExportAssignment(statement)) {
      body.push(`const __tn_default = ${statement.expression.getText(sourceFile)};`);
      exports.set("default", { localName: "__tn_default" });
      continue;
    }

    const modifiers = nodeModifiers(statement);
    const isExported = modifiers.some((modifier) => modifier.kind === ts.SyntaxKind.ExportKeyword);
    const isDefault = modifiers.some((modifier) => modifier.kind === ts.SyntaxKind.DefaultKeyword);
    const stripped = stripExportModifiers(statement, sourceFile);
    if (stripped.length > 0) {
      body.push(stripped);
    }
    if (!isExported) {
      continue;
    }
    const names = declarationNames(statement);
    for (const name of names) {
      exports.set(isDefault ? "default" : name, { localName: name });
    }
    if (isDefault && names.length === 0 && stripped.length > 0) {
      body[body.length - 1] = `const __tn_default = ${stripped};`;
      exports.set("default", { localName: "__tn_default" });
    }
  }

  return { body, dependencies, exports, imports, moduleName, starDependencies };
}

function generateModule(moduleName, data, moduleIndexes) {
  const prelude = [];
  for (const imported of data.imports) {
    const dependencyIndex = moduleIndexes.get(imported.dependency);
    if (dependencyIndex === undefined) {
      throw new Error(`Missing canonical stdlib dependency '${imported.dependency}' for '${moduleName}'.`);
    }
    const dependency = `__tn_stdlib_module_${dependencyIndex}`;
    const clause = imported.clause;
    if (clause.name !== undefined) {
      prelude.push(`const ${clause.name.text} = ${dependency}["default"];`);
    }
    if (clause.namedBindings !== undefined && ts.isNamespaceImport(clause.namedBindings)) {
      prelude.push(`const ${clause.namedBindings.name.text} = ${dependency};`);
    } else if (clause.namedBindings !== undefined && ts.isNamedImports(clause.namedBindings)) {
      for (const element of clause.namedBindings.elements) {
        if (element.isTypeOnly) {
          continue;
        }
        const importedName = element.propertyName?.text ?? element.name.text;
        prelude.push(`const ${element.name.text} = ${dependency}[${JSON.stringify(importedName)}];`);
      }
    }
  }

  const exportAssignments = [];
  for (const [name, value] of data.exports) {
    const expression = value.dependency === undefined
      ? value.localName
      : `__tn_stdlib_module_${moduleIndexes.get(value.dependency)}[${JSON.stringify(value.localName)}]`;
    exportAssignments.push(`__tn_exports[${JSON.stringify(name)}] = ${expression};`);
  }
  for (const dependency of data.starDependencies) {
    exportAssignments.push(`Object.assign(__tn_exports, __tn_stdlib_module_${moduleIndexes.get(dependency)});`);
  }

  const transpiled = ts.transpileModule([
    ...prelude,
    "const __tn_exports = {};",
    ...data.body,
    ...exportAssignments,
  ].join("\n"), {
    compilerOptions: {
      module: ts.ModuleKind.None,
      target: ts.ScriptTarget.ES2020,
    },
    fileName: `${moduleName}.ts`,
  }).outputText.trim();
  return `const __tn_stdlib_module_${moduleIndexes.get(moduleName)} = (() => {\n${indent(transpiled)}\n  return Object.freeze(__tn_exports);\n})();`;
}

function topologicalOrder(modules) {
  const indegree = new Map([...modules.keys()].map((name) => [name, modules.get(name).dependencies.size]));
  const dependents = new Map();
  for (const [name, data] of modules) {
    for (const dependency of data.dependencies) {
      const values = dependents.get(dependency) ?? new Set();
      values.add(name);
      dependents.set(dependency, values);
    }
  }
  const ready = [...indegree.entries()].filter(([, count]) => count === 0).map(([name]) => name).sort();
  const result = [];
  while (ready.length > 0) {
    const name = ready.shift();
    result.push(name);
    for (const dependent of [...(dependents.get(name) ?? [])].sort()) {
      const count = (indegree.get(dependent) ?? 0) - 1;
      indegree.set(dependent, count);
      if (count === 0) {
        const index = ready.findIndex((candidate) => candidate > dependent);
        ready.splice(index === -1 ? ready.length : index, 0, dependent);
      }
    }
  }
  if (result.length !== modules.size) {
    throw new Error("Canonical stdlib modules must form an acyclic graph.");
  }
  return result;
}

function resolveLocalModule(specifier) {
  if (typeof specifier !== "string" || !specifier.startsWith("./")) {
    return undefined;
  }
  const name = specifier.slice(2).replace(/\.js$/, "");
  return moduleNames.includes(name) ? name : undefined;
}

function literalSpecifier(node) {
  return ts.isStringLiteralLike(node) ? node.text : undefined;
}

function declarationNames(statement) {
  if ((ts.isFunctionDeclaration(statement) || ts.isClassDeclaration(statement) || ts.isEnumDeclaration(statement)) && statement.name !== undefined) {
    return [statement.name.text];
  }
  if (ts.isVariableStatement(statement)) {
    return statement.declarationList.declarations.flatMap((declaration) => ts.isIdentifier(declaration.name) ? [declaration.name.text] : []);
  }
  return [];
}

function nodeModifiers(statement) {
  return ts.canHaveModifiers(statement) ? ts.getModifiers(statement) ?? [] : [];
}

function stripExportModifiers(statement, sourceFile) {
  const statementStart = statement.getStart(sourceFile);
  const text = statement.getText(sourceFile);
  const ranges = nodeModifiers(statement)
    .filter((modifier) => modifier.kind === ts.SyntaxKind.ExportKeyword || modifier.kind === ts.SyntaxKind.DefaultKeyword)
    .map((modifier) => ({
      end: modifier.getEnd() - statementStart,
      start: modifier.getStart(sourceFile) - statementStart,
    }));
  return ranges.sort((left, right) => right.start - left.start).reduce((current, range) => `${current.slice(0, range.start)}${current.slice(range.end)}`, text).trim();
}

function indent(source) {
  return source.split("\n").map((line) => `  ${line}`).join("\n");
}
